#include<iostream>
using namespace std;
int main(){
	
	int a[2][2] = {
	 {1,1},
	 {1,0}
	};
	
	int b[2][2] = {{1,1},{1,0}};
	int n;
	cin >> n;
	for(int i = 0; i < n; i++){
		b[0][0] = (b[0][0]*a[0][0])+(b[0][1]*a[1][0]);
		b[0][1] = (b[0][0]*a[0][1])+(b[0][1]*a[1][1]);
		b[1][0] = (b[1][0]*a[0][0])+(b[1][1]*a[1][0]);
		b[1][1] = (b[1][0]*a[0][1])+(b[1][1]*a[1][1]);	
	}
	
	cout << b[1][1];
	return 0;
}
